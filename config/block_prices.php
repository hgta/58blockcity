/**
 * 计算区块价格（替代旧公式）
 * 对于A区使用精确公式，B-Z区使用价格查找表
 * 
 * A区公式: 1286 - (col-1) - (row-1)*12
 * 
 * @param string $zone 区域（A-H, Z）
 * @param string $blockNo 区块号（如 '0101'）
 * @return int 区块价格
 */
function calculateBlockPriceNew(string $zone, string $blockNo): int {
    $col = intval(substr($blockNo, 0, 2));
    $row = intval(substr($blockNo, 2, 2));
    
    if ($zone === 'A') {
        $price = 1286 - ($col - 1) - ($row - 1) * 12;
        return max($price, 1);
    }
    
    $price = getBlockPrice($blockNo);
    if ($price !== null) {
        return $price;
    }
    
    // 兜底：返回区域基准价（不应到达这里）
    $zoneConfig = require __DIR__ . '/zones.php';
    if (isset($zoneConfig[$zone])) {
        return $zoneConfig[$zone]['base_price'];
    }
    return 1000;
}